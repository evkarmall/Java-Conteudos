// Definindo a classe chamada Dados
public class Dados {
    // O método principal (ponto de entrada do programa)
    public static void main(String[] args) throws Exception {
       
        // Imprimindo uma mensagem na tela
        System.out.println("Trabalhando com Variáveis e Tipos de Dados");

        // Declarando e inicializando uma variável do tipo long
        long num1 = 10;
    
        // Declarando uma variável do tipo float e inicializando-a com um valor ponto flutuante
        float num2 = 28.3F;
    
        // Imprimindo o resultado de uma expressão aritmética (adição de num1 e num2)
        System.out.println(num1 + num2);
    
        // Declarando uma variável booleana e inicializando-a com o valor "true"
        boolean aprendendoJava = true;
        
        // Imprimindo o valor da variável booleana
        System.out.println(aprendendoJava);
    
        // Declarando uma variável do tipo char e inicializando-a com o caractere 'D'
        char letra = 'D';
    
        // Declarando e inicializando uma variável do tipo String
        String nome = "SA";
        
        // Imprimindo o resultado da concatenação do caractere e da String
        System.out.println(letra + nome);
    
        // Declarando e inicializando múltiplas variáveis do tipo int
        int num3 = 70, num4 = 19;
        
        // Imprimindo o resultado da soma das variáveis num3 e num4
        System.out.println(num3 + num4);
    
        // Declarando variáveis de outros tipos de dados
        boolean resultado = true;
        char capitalD = 'D';
        byte b = 100;
        short s = 10000;
        int i = 100000;
    }
}

/*Esse código demonstra conceitos básicos de Java, 
como declaração e inicialização de variáveis de diferentes tipos, 
operações aritméticas, manipulação de booleanos, caracteres e Strings, 
além de mostrar como imprimir valores no console. */